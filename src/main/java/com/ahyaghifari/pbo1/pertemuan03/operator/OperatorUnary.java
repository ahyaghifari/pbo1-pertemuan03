package com.ahyaghifari.pbo1.pertemuan03.operator;

public class OperatorUnary {
    public static void main(String[] args) {
        int i = 0;
        i++;
        System.out.println(i);
        i++;
        System.out.println(i);
        i = i+1;
        System.out.println(i);
        
        System.out.println();
        
        i+=3;
        System.out.println(i);
        
        i+=3;
        System.out.println(i);
        
        i*=3;
        System.out.println(i);
        
        i+=3;
        System.out.println(i);
        
    }
}
